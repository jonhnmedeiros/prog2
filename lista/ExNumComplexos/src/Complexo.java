public class Complexo {
    private float real,imag;
    
    public Complexo(float r, float i) {
        real = r;
        imag = i;
    }
    
    public float Modulo() {
        return real * imag;
    }
    
    public float Angulo() {
        return real / imag;
    }
    
    
    
    
    
    
    
    
}
